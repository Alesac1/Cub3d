#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	size_t	i;
	char	cc;

	i = ft_strlen(s) + 1;
	cc = (char) c;
	while (i--)
	{
		if (s[i] == cc)
			return ((char *) s + i);
	}
	return (NULL);
}
